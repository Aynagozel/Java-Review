package com.rewrite1;

public class WhileLoopPractice {
	public static void main(String[] args) {
		//how to print numbers dfrom 1 to 20
		
		int i=1;
		while(i<=20) {
			System.out.println(i);
			i++;
		}
		// i want to print numbers from 10 to 30 all in 1 line
		System.out.println("****************");
		
		int y=10;
		while(y<=30) {
			System.out.println(y+" ");
			y++;
		}
	
		System.out.println("========================");
		//how to print values from 10 to 1
		
		int a=10;
		while(a<=1) {
			System.out.println(a);
			a--;
		
	}
}
}