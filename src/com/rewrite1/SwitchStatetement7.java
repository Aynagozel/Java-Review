package com.rewrite1;

public class SwitchStatetement7 {
	public static void main(String[] args) {
		 int iSwitch=6;
		 switch(iSwitch) {
		 case 0:
			 System.out.println("ZERO");
			 break;
		 case 1:
			 System.out.println("ONE");
			 break;
		 case 2:
			 System.out.println("TWO");
			 break;
		 default:
			 System.out.println("Not in the list");
			 break;
			 
	}
}
}